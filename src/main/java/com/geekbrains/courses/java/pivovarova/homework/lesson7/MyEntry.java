package com.geekbrains.courses.java.pivovarova.homework.lesson7;

public class MyEntry {
    Integer key;
    Integer value;

    public MyEntry(Integer key, Integer value) {
        this.key = key;
        this.value = value;
    }

    public Integer getKey() {
        return key;
    }

    public Integer getValue() {
        return value;
    }
}
